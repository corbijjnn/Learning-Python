# Learning how to use comments to better understand my own and others code.

'''This is a multi-line comment. It can be used 
to explain complex logic or provide context for a block of code.'''

quantity = 10  # This is a single-line comment explaining the variable 'quantity'
unit_price = 2.5  # This is a single-line comment explaining the variable 'unit_price'
extended_price = quantity * unit_price  # This calculates the total price by multiplying quantity and unit price
print(f"The extended price is: ${extended_price:.2f}")  # This prints the extended price formatted to 2 decimal places
